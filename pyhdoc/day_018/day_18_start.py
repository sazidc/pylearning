# Using turtle module to make a square

import turtle as timmy_the_turtle

timmy_the_turtle.color("coral")
timmy_the_turtle.forward(100)
timmy_the_turtle.right(90)
timmy_the_turtle.forward(100)
timmy_the_turtle.right(90)
timmy_the_turtle.forward(100)
timmy_the_turtle.right(90)
timmy_the_turtle.forward(100)
timmy_the_turtle.right(90)

timmy_the_turtle.exitonclick()
